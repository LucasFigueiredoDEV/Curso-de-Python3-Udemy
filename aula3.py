"""
Python = Linguagem de programação
Tipo de tipagem = dinâmica / forte
str -> string -> texto
String são textos que estão dentro de aspas
"""

#Aspas simples
print('Lucas')

#Aspas duplas
print("Lucas")

#Escape (Barra invertida para ignorar a aspa dupla entre Lucas e Samuel, e imprimir a aspa )
print("Lucas\" Samuel")

#r (serve para exibir o caractere de escape, ou seja a barra invertida será imprimida também)
print(r"Lucas\" Samuel")