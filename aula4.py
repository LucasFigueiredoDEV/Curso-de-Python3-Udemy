#Tipos int e float
#int -> Número inteiro
#O tipo int representa qualquer numero
#positivo ou negativo
#positivo
print(11) #int
print(-11) #int
print(0) #int

#float -> Número com ponto flutuante.
#O tipo float representa qualquer número.
#Posaitivo ou negativo com ponto flutuante.
#Float sem sinal é considerado positivo
print(1.1, 10.11) #float
print(0.0) #float
print(-1.5) #float

#A função type mostra o tipo que o pythpn inferiu ao valor
print( type("Lucas"))
print( type(20))
print( type(2.0), type(-1.1), type(0.0))
